#!/usr/bin/env python3
"""Extract and calculate methylation beta values from target and background bedgraph files.

This script processes methylation data from bedgraph files generated by methyldackel,
merges target and background data, calculates raw beta values, and filters by CpG list.
"""

import sys
from pathlib import Path
from typing import Tuple

import click
import pandas as pd
from rich.console import Console
from rich.progress import Progress, SpinnerColumn, TextColumn, BarColumn, TimeElapsedColumn

# Initialize rich console for output
console = Console()


def read_bedgraph(
    bedgraph_path: Path,
    prefix: str,
    progress: Progress,
    task_id: int
) -> pd.DataFrame:
    """Read a bedgraph file into a pandas DataFrame.

    Args:
        bedgraph_path: Path to the bedgraph file.
        prefix: Prefix for column names ('target' or 'background').
        progress: Rich Progress object for tracking.
        task_id: Task ID for progress tracking.

    Returns:
        DataFrame with columns: chr, start, end, {prefix}_beta, {prefix}_meth_count, {prefix}_unmeth_count.

    Raises:
        FileNotFoundError: If the bedgraph file does not exist.
        ValueError: If the bedgraph file format is invalid.
    """
    if not bedgraph_path.exists():
        raise FileNotFoundError(f"Bedgraph file not found: {bedgraph_path}")
    
    try:
        progress.update(task_id, description=f"[cyan]Reading {prefix} bedgraph...")
        
        # Read bedgraph file with comment='track' to skip track lines
        df = pd.read_csv(
            bedgraph_path,
            sep='\t',
            header=None,
            skiprows=1,
            names=['chr', 'start', 'end', f'{prefix}_beta', f'{prefix}_meth_count', f'{prefix}_unmeth_count'],
            dtype={
                'chr': str,
                'start': int,
                'end': int,
                f'{prefix}_beta': float,
                f'{prefix}_meth_count': int,
                f'{prefix}_unmeth_count': int
            }
        )

        df['end'] = df['end'] - 1
        
        if df.empty:
            console.print(f"[yellow]Warning: {prefix} bedgraph file is empty[/yellow]")
        
        progress.update(task_id, description=f"[green]✓ Read {prefix} bedgraph ({len(df):,} rows)")
        return df
        
    except Exception as e:
        raise ValueError(f"Error reading {prefix} bedgraph file: {str(e)}")


def read_cpg_list(cpg_list_path: Path, progress: Progress, task_id: int) -> pd.DataFrame:
    """Read CpG list file into a pandas DataFrame.

    Args:
        cpg_list_path: Path to the CpG list file.
        progress: Rich Progress object for tracking.
        task_id: Task ID for progress tracking.

    Returns:
        DataFrame with columns: chr, start, end, final_recall, meandiff.

    Raises:
        FileNotFoundError: If the CpG list file does not exist.
        ValueError: If the CpG list file format is invalid.
    """
    if not cpg_list_path.exists():
        raise FileNotFoundError(f"CpG list file not found: {cpg_list_path}")
    
    try:
        progress.update(task_id, description="[cyan]Reading CpG list...")
        
        # Read CpG list file
        df = pd.read_csv(
            cpg_list_path,
            sep='\t',
            usecols=['chr', 'start', 'end', 'final_recall', 'meandiff'],
            dtype={
                'chr': str,
                'start': int,
                'end': int,
                'final_recall': float,
                'meandiff': float
            }
        )
        
        if df.empty:
            raise ValueError("CpG list file is empty")
        
        progress.update(task_id, description=f"[green]✓ Read CpG list ({len(df):,} rows)")
        return df
        
    except Exception as e:
        raise ValueError(f"Error reading CpG list file: {str(e)}")


def merge_target_background(
    target_df: pd.DataFrame,
    background_df: pd.DataFrame,
    progress: Progress,
    task_id: int
) -> pd.DataFrame:
    """Merge target and background dataframes in outer mode.

    Args:
        target_df: Target bedgraph DataFrame.
        background_df: Background bedgraph DataFrame.
        progress: Rich Progress object for tracking.
        task_id: Task ID for progress tracking.

    Returns:
        Merged DataFrame with NaN values filled with 0.

    Raises:
        ValueError: If merge operation fails.
    """
    try:
        progress.update(task_id, description="[cyan]Merging target and background...")
        
        # Perform outer merge on chr, start, end
        merged_df = pd.merge(
            target_df,
            background_df,
            on=['chr', 'start', 'end'],
            how='outer'
        )
        
        # Fill NaN values with 0
        merged_df = merged_df.fillna(0)
        
        # Convert count columns back to int after fillna
        count_columns = [
            'target_meth_count', 'target_unmeth_count',
            'background_meth_count', 'background_unmeth_count'
        ]
        for col in count_columns:
            merged_df[col] = merged_df[col].astype(int)
        
        progress.update(task_id, description=f"[green]✓ Merged dataframes ({len(merged_df):,} rows)")
        return merged_df
        
    except Exception as e:
        raise ValueError(f"Error merging target and background: {str(e)}")


def calculate_raw_beta(merged_df: pd.DataFrame, progress: Progress, task_id: int) -> pd.DataFrame:
    """Calculate raw beta values and raw counts from merged data.

    Args:
        merged_df: Merged DataFrame with target and background data.
        progress: Rich Progress object for tracking.
        task_id: Task ID for progress tracking.

    Returns:
        DataFrame with additional columns: raw_meth_count, raw_unmeth_count, raw_total_count, raw_beta.

    Raises:
        ValueError: If calculation fails.
    """
    try:
        progress.update(task_id, description="[cyan]Calculating raw beta values...")
        
        # Calculate raw counts by summing target and background
        merged_df['raw_meth_count'] = (
            merged_df['target_meth_count'] + merged_df['background_meth_count']
        )
        merged_df['raw_unmeth_count'] = (
            merged_df['target_unmeth_count'] + merged_df['background_unmeth_count']
        )
        merged_df['raw_total_count'] = (
            merged_df['raw_meth_count'] + merged_df['raw_unmeth_count']
        )
        
        # Calculate raw beta value (avoid division by zero)
        # Where total count is 0, set beta to 0
        merged_df['raw_beta'] = 0.0
        mask = merged_df['raw_total_count'] > 0
        merged_df.loc[mask, 'raw_beta'] = (
            merged_df.loc[mask, 'raw_meth_count'] / merged_df.loc[mask, 'raw_total_count']
        )
        
        progress.update(task_id, description="[green]✓ Calculated raw beta values")
        return merged_df
        
    except Exception as e:
        raise ValueError(f"Error calculating raw beta values: {str(e)}")


def filter_by_cpg_list(
    merged_df: pd.DataFrame,
    cpg_df: pd.DataFrame,
    progress: Progress,
    task_id: int
) -> pd.DataFrame:
    """Filter merged data by CpG list using inner merge.

    Args:
        merged_df: Merged DataFrame with calculated beta values.
        cpg_df: CpG list DataFrame.
        progress: Rich Progress object for tracking.
        task_id: Task ID for progress tracking.

    Returns:
        Filtered DataFrame containing only CpGs in the list.

    Raises:
        ValueError: If filtering operation fails.
    """
    try:
        progress.update(task_id, description="[cyan]Filtering by CpG list...")
        
        # Perform inner merge on chr, start, end
        filtered_df = pd.merge(
            merged_df,
            cpg_df,
            on=['chr', 'start', 'end'],
            how='inner'
        )
        
        if filtered_df.empty:
            console.print("[yellow]Warning: No CpGs matched between data and CpG list[/yellow]")
        
        progress.update(task_id, description=f"[green]✓ Filtered by CpG list ({len(filtered_df):,} rows)")
        return filtered_df
        
    except Exception as e:
        raise ValueError(f"Error filtering by CpG list: {str(e)}")


def write_output(output_df: pd.DataFrame, output_path: Path, progress: Progress, task_id: int) -> None:
    """Write output DataFrame to compressed gzip TSV file.

    Args:
        output_df: DataFrame to write.
        output_path: Path to output file.
        progress: Rich Progress object for tracking.
        task_id: Task ID for progress tracking.

    Raises:
        IOError: If writing fails.
    """
    try:
        progress.update(task_id, description="[cyan]Writing output file...")
        
        # Ensure output directory exists
        output_path.parent.mkdir(parents=True, exist_ok=True)
        
        # Write to compressed TSV
        output_df.to_csv(
            output_path,
            sep='\t',
            index=False,
            compression='gzip'
        )
        
        # Get file size for reporting
        file_size = output_path.stat().st_size
        size_mb = file_size / (1024 * 1024)
        
        progress.update(
            task_id,
            description=f"[green]✓ Wrote output file ({size_mb:.2f} MB)"
        )
        
    except Exception as e:
        raise IOError(f"Error writing output file: {str(e)}")


@click.command()
@click.option(
    '--target-bedgraph',
    type=click.Path(exists=True, path_type=Path),
    required=True,
    help='Target bedgraph file generated by methyldackel'
)
@click.option(
    '--background-bedgraph',
    type=click.Path(exists=True, path_type=Path),
    required=True,
    help='Background bedgraph file generated by methyldackel'
)
@click.option(
    '--cpg-list',
    type=click.Path(exists=True, path_type=Path),
    required=True,
    help='CpG list file with columns: chr, start, end, final_recall, meandiff'
)
@click.option(
    '--output-prefix',
    type=click.Path(path_type=Path),
    required=True,
    help='Output prefix for beta value file (will be compressed with gzip)'
)
@click.option(
    '--ncpus',
    type=int,
    default=1,
    help='Number of CPUs to use (currently not implemented for multi-threading)'
)
def main(
    target_bedgraph: Path,
    background_bedgraph: Path,
    cpg_list: Path,
    output_prefix: Path,
    ncpus: int
) -> None:
    """Extract beta value from target and background bedgraph files.

    This script processes methylation data from target and background bedgraph files,
    merges them, and filters by a CpG list.

    Args:
        target_bedgraph: Path to target bedgraph file.
        background_bedgraph: Path to background bedgraph file.
        cpg_list: Path to CpG list file.
        output_prefix: Path to output file.
        ncpus: Number of CPUs (for future multi-threading support).

    Examples:
        $ python extract_beta_value.py \\
            --target-bedgraph target.bedgraph \\
            --background-bedgraph background.bedgraph \\
            --cpg-list cpg_list.txt \\
            --output beta_value.tsv.gz \\
            --ncpus 4
    """
    console.print("[bold blue]Methylation Beta Value Extraction[/bold blue]")
    console.print(f"Target bedgraph: {target_bedgraph}")
    console.print(f"Background bedgraph: {background_bedgraph}")
    console.print(f"CpG list: {cpg_list}")
    console.print(f"Output prefix: {output_prefix}")
    console.print(f"CPUs: {ncpus}")
    console.print()

    output = Path(f"{output_prefix}_beta_value.tsv.gz")
    
    try:
        with Progress(
            SpinnerColumn(),
            TextColumn("[progress.description]{task.description}"),
            BarColumn(),
            TimeElapsedColumn(),
            console=console
        ) as progress:
            
            # Create task placeholders
            task1 = progress.add_task("[cyan]Initializing...", total=None)
            
            # Step 1: Read target bedgraph
            target_df = read_bedgraph(target_bedgraph, 'target', progress, task1)
            
            # Step 2: Read background bedgraph
            task2 = progress.add_task("[cyan]Initializing...", total=None)
            background_df = read_bedgraph(background_bedgraph, 'background', progress, task2)
            
            # Step 3: Read CpG list
            task3 = progress.add_task("[cyan]Initializing...", total=None)
            cpg_df = read_cpg_list(cpg_list, progress, task3)
            
            # Step 4: Merge target and background
            task4 = progress.add_task("[cyan]Initializing...", total=None)
            merged_df = merge_target_background(target_df, background_df, progress, task4)
            
            # Step 5: Calculate raw beta values
            task5 = progress.add_task("[cyan]Initializing...", total=None)
            merged_df = calculate_raw_beta(merged_df, progress, task5)
            
            # Step 6: Filter by CpG list
            task6 = progress.add_task("[cyan]Initializing...", total=None)
            final_df = filter_by_cpg_list(merged_df, cpg_df, progress, task6)
            
            # Step 7: Write output
            task7 = progress.add_task("[cyan]Initializing...", total=None)
            write_output(final_df, output, progress, task7)
        
        console.print()
        console.print("[bold green]✓ Successfully completed![/bold green]")
        console.print(f"Output file: {output}")
        console.print(f"Total CpGs: {len(final_df):,}")
        
    except FileNotFoundError as e:
        console.print(f"[bold red]Error:[/bold red] {str(e)}", style="red")
        sys.exit(1)
    except ValueError as e:
        console.print(f"[bold red]Error:[/bold red] {str(e)}", style="red")
        sys.exit(1)
    except IOError as e:
        console.print(f"[bold red]Error:[/bold red] {str(e)}", style="red")
        sys.exit(1)
    except Exception as e:
        console.print(f"[bold red]Unexpected error:[/bold red] {str(e)}", style="red")
        sys.exit(1)


if __name__ == '__main__':
    main()

